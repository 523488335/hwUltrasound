package com.hw.bean;

public class Point2d {

	private long x;
	private float y;
	public long getX() {
		return x;
	}
	public void setX(long x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public Point2d(long x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
}
